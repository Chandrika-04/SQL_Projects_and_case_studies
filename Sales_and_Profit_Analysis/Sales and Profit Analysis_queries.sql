USE sales_case_study;

-- =========================================================
-- TASK 1
-- Display the number of states present in the LocationTable
-- =========================================================
select Count(distinct state) as num_of_states
from locationtable;

-- =========================================================
-- TASK 2
-- How many products are of regular type?
-- =========================================================

select Sum(case when Type='Regular' then 1 else 0 end ) as regular_products_count
from producttable;

-- =========================================================
-- TASK 3
-- How much spending has been done on marketing of ProductID 1?
-- =========================================================
select total_expenses
from facttable
where productId=1;

-- =========================================================
-- TASK 4
-- What is the minimum sales of a product?
-- =========================================================
select MIN(sales) as minimum_sale_for_product
from facttable;

-- =========================================================
-- TASK 5
-- Display the maximum Cost Of Goods Sold (COGS)
-- =========================================================
select MAX(COGS) as maximum_cost_of_goods_sold
from facttable;

-- =========================================================
-- TASK 6
-- Display the details of the product where ProductType is Coffee
-- =========================================================
select * from producttable
where ProductType='Coffee';

-- =========================================================
-- TASK 7
-- Display the details where Total Expenses are greater than 40
-- =========================================================
select *
from facttable
where Total_Expenses>40;

-- =========================================================
-- TASK 8
-- What is the average sales in Area Code 719?
-- =========================================================
select AVG(sales) as avg_sales from facttable
where Area_Code=719;

-- =========================================================
-- TASK 9
-- Find out the total profit generated by Colorado state
-- =========================================================
select l.state,SUM(f.profit) as total_profit 
from facttable f join locationtable l
on f.Area_Code=l.Area_Code
where state='Colorado'
group by l.State;

-- =========================================================
-- TASK 10
-- Display the average inventory for each ProductID
-- =========================================================
select ProductID,AVG(Inventory) as avg_inventory 
from facttable
group by ProductID;

-- =========================================================
-- TASK 11
-- Display states in sequential order in the LocationTable
-- =========================================================
select state from locationtable
order by state asc;

-- =========================================================
-- TASK 12
-- Display the average budget margin where average budget margin
-- should be greater than 100
-- =========================================================
select ProductID,avg(budget_margin) as avg_budget_margin 
from facttable
group by ProductID
having avg(Budget_Margin)>100;

-- =========================================================
-- TASK 13
-- What is the total sales done on date 2010-01-01?
-- =========================================================
select date_format(date,'%Y-%m') as date,SUM(sales) as total_sales 
from facttable
where date='2010-01-01'
group by date;

-- =========================================================
-- TASK 14
-- Display the average total expenses of each ProductID
-- on an individual date
-- =========================================================
select productID,date_format(date,'%Y-%m') as date,avg(total_expenses) as avg_total_expense
from facttable
group by ProductID,date;
-- =========================================================
-- TASK 15-- Display the table with the following attributes:
-- Date, ProductID, ProductType, Product, Sales,-- Profit, State, Area_Code
-- ========================================================
select f.date,f.productid,p.producttype,p.product,f.sales,f.profit,l.state,f.area_code
from facttable f join producttable p
on f.ProductID=p.ProductID
join locationtable l on l.Area_Code=f.Area_Code;
-- =========================================================
-- TASK 16
-- Display the rank without any gap to show the sales-wise rank
-- =========================================================
select f.productID,p.product,f.sales,rank() over (order by f.sales desc) as sales_rank
from facttable f join producttable p
on f.ProductID=p.ProductID;
-- =========================================================
-- TASK 17
-- Find the state-wise profit and sales
-- =========================================================
select l.state, SUM(f.profit) as total_profit, SUM(sales) as total_sales
from facttable f join locationtable l
on l.Area_Code=f.Area_Code
group by l.State;
-- =========================================================
-- TASK 18
-- Find the state-wise profit and sales along with product name
-- =========================================================
select p.product,l.state, SUM(f.profit) as total_profit, SUM(sales) as total_sales
from facttable f join locationtable l
on l.Area_Code=f.Area_Code
join producttable p
on f.ProductID=p.ProductID
group by l.State,p.Product;
-- =========================================================
-- TASK 19
-- If there is an increase in sales of 5%,
-- calculate the increased sales
-- =========================================================
select productID,sales,sales+sales*0.05 as increased_sales
from facttable;

-- =========================================================
-- TASK 20
-- Find the maximum profit along with ProductID
-- and ProductType
-- =========================================================
select f.productID,P.Producttype,max(f.profit) as maximum_profit
from facttable f join producttable p
on f.ProductID=p.ProductID
group by f.productID,P.Producttype;

