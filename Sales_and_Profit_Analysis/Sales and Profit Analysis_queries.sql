-- TASK 22
-- Create a condition:
-- If Total_Expenses is less than 60 then 'Profit'
-- else 'Loss'
-- =========================================================
select ProductID,Total_Expenses,
case when Total_Expenses<60 then 'loss '
else 'profit' end as  Profit_or_Loss
from facttable;
-- =========================================================
-- TASK 23
-- Give the total weekly sales value with Date
-- and ProductID details using ROLLUP
-- =========================================================
select productId, date,sum(sales) as total_weekly_sales
from facttable
GROUP BY Date, ProductID WITH ROLLUP;
-- =========================================================
-- TASK 24
-- Apply UNION and INTERSECTION operator on tables
-- consisting of Area_Code attribute
-- =========================================================
select Area_Code from facttable
union
select Area_Code from locationtable;

select distinct f.area_code
from facttable f join locationtable l
ON f.Area_Code = l.Area_Code;
